package conn.bean;

public class BNota {

    private int notaCod;
    private int alumnoCod;
    private int notaPuntualidad;
    private int notaDesempeno;
    private int notaDesenvolvimiento;
    private int notaEmpleador;
    private String comentarios;

    public int getNotaCod() {
        return notaCod;
    }

    public void setNotaCod(int notaCod) {
        this.notaCod = notaCod;
    }

    public int getAlumnoCod() {
        return alumnoCod;
    }

    public void setAlumnoCod(int alumnoCod) {
        this.alumnoCod = alumnoCod;
    }

    public int getNotaPuntualidad() {
        return notaPuntualidad;
    }

    public void setNotaPuntualidad(int notaPuntualidad) {
        this.notaPuntualidad = notaPuntualidad;
    }

    public int getNotaDesempeno() {
        return notaDesempeno;
    }

    public void setNotaDesempeno(int notaDesempeno) {
        this.notaDesempeno = notaDesempeno;
    }

    public int getNotaDesenvolvimiento() {
        return notaDesenvolvimiento;
    }

    public void setNotaDesenvolvimiento(int notaDesenvolvimiento) {
        this.notaDesenvolvimiento = notaDesenvolvimiento;
    }

    public int getNotaEmpleador() {
        return notaEmpleador;
    }

    public void setNotaEmpleador(int notaEmpleador) {
        this.notaEmpleador = notaEmpleador;
    }

    public String getComentarios() {
        return comentarios;
    }

    public void setComentarios(String comentarios) {
        this.comentarios = comentarios;
    }

}
